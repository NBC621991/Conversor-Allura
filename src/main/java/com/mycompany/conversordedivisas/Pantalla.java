    private void ChooseConverItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_ChooseConverItemStateChanged
        if(evt.getStateChange() == ItemEvent.SELECTED){
            if(this.ChooseConver.getSelectedIndex() > 0)
            {
                this.De.setModel(new DefaultComboBoxModel(this.getConversor(this.ChooseConver.getSelectedItem().toString())));

            }
            if(evt.getStateChange() == ItemEvent.SELECTED){
                if(this.ChooseConver.getSelectedIndex() > 0)
                {
                    this.A.setModel(new DefaultComboBoxModel(this.getConversor(this.ChooseConver.getSelectedItem().toString())));
                }
            }
        }
    }//GEN-LAST:event_ChooseConverItemStateChanged


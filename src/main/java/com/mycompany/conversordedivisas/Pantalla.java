    public static boolean validarNumeros(String datoIngresado){
        
        return datoIngresado.matches("[0.0000-9.0000000]{1,1000000}");
    }
    
    
    public String[]  getConversor(String tipo){
        {
            
            String[] tipoConver = new String[7];
            if(tipo.equalsIgnoreCase("Moneda")){
            
                    tipoConver[0] = "Seleccionar";
                    tipoConver[1] = "Peso Mexicano";
                    tipoConver[2] = "Dolar";
                    tipoConver[3] = "Euro";
                    tipoConver[4] = "Libras Esterlinas";
                    tipoConver[5] = "Yen Japones";
                    tipoConver[6] = "Won sur-coreano";
                    }
    
            if(tipo.equalsIgnoreCase("Distancia")){
            
                    tipoConver[0] = "Seleccionar";
                    tipoConver[1] = "Metro";
                    tipoConver[2] = "Centimetro";
                    tipoConver[3] = "Micrometro";
                    tipoConver[4] = "Milla";
                    tipoConver[5] = "Yarda";
                    tipoConver[6] = "Pie";
                    
        }
        return tipoConver;
        }
}

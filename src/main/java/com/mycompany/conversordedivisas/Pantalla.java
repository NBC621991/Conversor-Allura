    private void BttConvActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BttConvActionPerformed

        Operaciones puente = new Operaciones();

        BIngresar.getText();

        if(!validarNumeros(BIngresar.getText().trim())){

            JOptionPane.showMessageDialog(null,"Ingrese un valor o revise que el valor ingresado sea un numero");

        }else{

            if(ChooseConver.getSelectedIndex() == 1){
                datoIngresado = Float.parseFloat(BIngresar.getText());
                conversionDe = De.getSelectedIndex();
                conversionA = A.getSelectedIndex();

                puente.setValor(datoIngresado);
                puente.setDivisaInicial(conversionDe);
                puente.setDivisaFinal(conversionA);
                float resultadoFinal = puente.convertir();

                BResultado.setText(Float.toString(resultadoFinal));
            }
            else if(ChooseConver.getSelectedIndex() == 2){
                datoIngresado = Float.parseFloat(BIngresar.getText());
                conversionDe = De.getSelectedIndex();
                conversionA = A.getSelectedIndex();

                puente.setValor(datoIngresado);
                puente.setDistanciaInicial(conversionDe);
                puente.setDistanciaFinal(conversionA);
                float resultadoFinal = puente.convertirDistancia();

                BResultado.setText(Float.toString(resultadoFinal));

            }
        }
    }//GEN-LAST:event_BttConvActionPerformed

